package exercicio_5;

public class Pessoa {
	private Double altura;
	private Integer sexo;
	
	public Pessoa(Double altura, Integer sexo) {
		this.altura = altura;
		this.sexo = sexo;
	}

	public Double getAltura() {
		return altura;
	}

	public void setAltura(Double altura) {
		this.altura = altura;
	}

	public Integer getSexo() {
		return sexo;
	}

	public void setSexo(Integer sexo) {
		this.sexo = sexo;
	}
	
	
}
