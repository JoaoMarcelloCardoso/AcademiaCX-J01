package com.academiacx.model;

public class Pessoa {
    private int altura;
    private String sexo;

    public Pessoa() {

    }

    public int getAltura() {
        return altura;
    }

    public void setAltura(int altura) {
        this.altura = altura;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }
}
